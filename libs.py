def slugify(name):
    """
    Преобразует русское название канала в безопасное имя .ts файла на английском.
    Пример: 'Первый канал' → 'perviy_kanal.ts'
    """
    # Транслитерация кириллицы в латиницу
    translit_map = {
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
        'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
        'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
        'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'shch',
        'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
        ' ': '_', '-': '_', '.': '', ',': '', '(': '', ')': '', '!': '',
        '?': '', '"': '', "'": '', '[': '', ']': '', '{': '', '}': '',
        '@': '', '#': '', '$': '', '%': '', '^': '', '&': '', '*': '',
        '+': '_', '=': '_', '`': '', '~': '', '|': '_', '\\': '_'
    }
    
    name = name.strip().lower()
    translated = ''.join(translit_map.get(char, '_') for char in name)
    
    # Убираем множественные подчёркивания
    import re
    translated = re.sub(r'_+', '_', translated)
    
    return translated.strip('_')[:50] + ".ts"

