def slugify(name):
    """
    Преобразует любое название в безопасное имя файла .ts
    - Сохраняет английские буквы и цифры
    - Транслитерирует кириллицу
    - Заменяет всё остальное на '_'
    - Удаляет дубли '_'
    Пример: 'МТВ Россия' → 'mtv_rossiya.ts'
    """
    # Полная карта транслитерации (кириллица → латиница)
    translit_map = {
        # Строчные
        'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
        'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
        'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
        'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'shch',
        'ъ': '', 'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
        # Заглавные
        'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo',
        'Ж': 'Zh', 'З': 'Z', 'И': 'I', 'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M',
        'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T', 'У': 'U',
        'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Shch',
        'Ъ': '', 'Ы': 'Y', 'Ь': '', 'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya'
    }

    result = ''
    for char in name:
        if char.isascii() and char.isalnum():
            # Английские буквы и цифры — оставляем
            result += char.lower()
        elif char in translit_map:
            # Русские — транслитерируем
            result += translit_map[char]
        else:
            # Всё остальное — подчёркивание
            result += '_'

    # Убираем множественные подчёркивания
    import re
    result = re.sub(r'_+', '_', result)

    return result.strip('_')[:50] + ".ts"